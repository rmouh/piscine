int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (*str == '\0')
		return (1);
	else
	{
		while (str[i])
		{
			if (str[i] > '~' || str[i] < ' ')
			{
				return (0);
			}
			else
				i++;
		}
		return (1);
	}
}
