#include<unistd.h>

void	ft_putnbr(int nb)
{
	int	temp;

	temp = nb;
	if (nb == -2147483648)
	{
		write (1, "-2147", 5);
		write (1, "483648", 6);
	}
	else
	{
		if (temp < 0)
		{
			write (1, "-", 1);
			temp = temp * -1;
		}
		if (temp / 10 != 0)
			ft_putnbr (temp / 10);
		temp = temp % 10 + 48;
		write (1, &temp, 1);
	}
}
